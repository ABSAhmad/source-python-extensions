#include "esx_signature_manager.h"

CSigger* gSigger;

//=============================================================================
// Constructor
//=============================================================================
CSigger::CSigger(void *addr)
{
	m_addr = addr;
}

//=============================================================================//
// Finds and returns the address of a signature
//=============================================================================//
void* CSigger::findFunction(const char *sig, int length)
{
	return g_SigMngr.ResolveSig( m_addr, sig, length );
}
